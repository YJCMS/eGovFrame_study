@Service("teachService")
public class TeachServiceImpl extends EgovAbstractServiceImpl implements TeachService {

	private static final Logger LOGGER = LoggerFactory.getLogger(TeachServiceImpl.class);

	/** SampleDAO */
	// TODO mybatis 사용
	@Resource(name="teachMapper")
	private TeachMapper teachMapper;

	/**
	 * 글 목록을 조회한다.
	 * @param searchVO - 조회할 정보가 담긴 VO
	 * @return 글 목록
	 * @exception Exception
	 */
	@Override
	public List<?> selectSampleDataList(HashMap<String, Object> paramMap) throws Exception {
		return teachMapper.selectSampleDataList(paramMap);
	}
	
	/**
	 * 글을 등록한다.
	 * @param vo - 등록할 정보가 담긴 SampleVO
	 * @return 등록 결과
	 * @exception Exception
	 */
	@Override
	public int insertData(HashMap<String, Object> paramMap) throws Exception {
		return teachMapper.insertData(paramMap);
	}
	
	/**
	 * 글을 수정한다.
	 * @param vo - 수정할 정보가 담긴 SampleVO
	 * @return void형
	 * @exception Exception
	 */
	@Override
	public int updateData(HashMap<String, Object> paramMap) throws Exception {
		int cnt = teachMapper.updateData(paramMap);
		return cnt;
	}
	
	/**
	 * 글을 삭제한다.
	 * @param vo - 삭제할 정보가 담긴 SampleVO
	 * @return void형
	 * @exception Exception
	 */
	@Override
	public int deleteData(HashMap<String, Object> paramMap) throws Exception {
		int cnt = teachMapper.deleteData(paramMap);
		return cnt;
	}
	
	/**
	 * 글을 삭제한다.
	 * @param vo - 삭제할 정보가 담긴 SampleVO
	 * @return void형
	 * @exception Exception
	 */
	@Override
	public int deleteYnData(HashMap<String, Object> paramMap) throws Exception {
		int cnt = teachMapper.deleteYnData(paramMap);
		return cnt;
	}

}
